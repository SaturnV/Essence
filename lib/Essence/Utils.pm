#! /usr/bin/perl
###### NAMESPACE ##############################################################

package Essence::Utils;

###### IMPORTS ################################################################

use Essence::Strict;

###### EXPORTS ################################################################

use Exporter qw( import );

our @EXPORT_OK = qw( pick );

###### SUBS ###################################################################

sub pick
{
  my $from = shift;
  return map { ($_ => $from->{$_}) } @_ if wantarray;
  return { map { ($_ => $from->{$_}) } @_ };
}

###############################################################################

1
